# Implementación de una cola
cola = []

# Agregar elementos (enqueue)
cola.append(1)
cola.append(2)
cola.append(3)
print("Cola después de agregar elementos:", cola)

# Eliminar elementos (dequeue)
primero = cola.pop(0)
print("Elemento eliminado:", primero)
print("Cola después de eliminar un elemento:", cola)
