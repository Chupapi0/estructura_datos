# Implementación de una pila
pila = []

# Agregar elementos (push)
pila.append(1)
pila.append(2)
pila.append(3)
print("Pila después de agregar elementos:", pila)

# Eliminar elementos (pop)
ultimo = pila.pop()
print("Elemento eliminado:", ultimo)
print("Pila después de eliminar un elemento:", pila)
